#이렇게 저장해두면 CMD에서 해당 파일을 호출 했을때 type을 사용했던것 처럼 인수를 줄때마다 argv에 하나씩 담는다 
# 해당 파일의 호출은 python sys1.py 인수1 인수2 ... 와 같은 방식으로 사용하고 sys1.py가 argv[0]에 담기고 인수1, 인수2 가 각각 argv[1], argv[2]에 담기게 되고 
# 인수1, 인수2 가 출력되게 될것이다.

#현재 파일을 저장하고 cmd에서 파일 위치로 가서 python sys1.py(지금 이 파일의 명) 인수1 인수2 ..를 입력해보자.

# 위 사용법을 응용해서 간단한 프로그램을 만들어보자

import sys

args = sys.argv[1:]

for i in args :
    print(i.upper(), end=" ")

# 입력한 인수를 대문자로 모두 바꾸며 한줄로 출력해주는 애플리케이션이다.
