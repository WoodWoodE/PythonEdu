#프로그램의 입출력

# 명령 프롬프트(CMD)에서 type 파일위치/파일명 를 작성하면 해당 파일의 내용을 출력해준다.
# type은 바로 뒤에 적인 파일 이름을 인수로 받아 파일의 내용을 출력해주는 명령어이다.
# 대부분의 명령 프롬프트에서 사용되는 명령어는 아래와 같이 인수를 전달하여 프로그램을 실행하는 방식을 따른다.

# 명령어 [인수1, 인수2, ...]

# 이런 기능을 파이썬에서도 사용할 수 있다.

