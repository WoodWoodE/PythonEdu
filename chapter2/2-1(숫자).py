#숫자형

######################
# - 정수형
a = 123
print(type(a));

# - 실수형
b = 1.23
print(type(b));

c = 1.23E10 # 1.23의 10제곱
print(type(c))
print(c)

# - 8진수
d = 0o10
print(type(d))
print(d)


# - 16진수
e = 0x16A
print(type(e))
print(e)

