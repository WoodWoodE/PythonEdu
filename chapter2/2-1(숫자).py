# 사칙연산

a1 = 3
b1 = 2
print(a1 + b1);   # 더하기
print(a1 - b1);   # 빼기
print(a1 * b1);   # 곱하기
print(a1 / b1);   # 나누기(자바는 %)
print(a1 // b1);  # 몫 
print(a1 ** b1);  # a의 b제곱을 의미하는 ** 연산자 
print(a1 % b1);   # 나머지

